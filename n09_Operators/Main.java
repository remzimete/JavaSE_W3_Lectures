package n09_Operators;

public class Main {
    public static void main(String[] args) {

        int numberOne,numberTwo,numberThree,numberFour;
        numberOne = 10;
        numberTwo = 10;
        numberThree = 10;
        numberFour = 10;

        int sum;

        //addition
        sum = numberOne + numberTwo;
        System.out.println("Addition : "+sum);
        //subtraction
        sum = numberOne - numberTwo;
        System.out.println("Subtraction : "+sum);
        //division
        sum = numberOne / numberTwo;
        System.out.println("Division : "+sum);
        //multiplication
        sum = numberOne * numberTwo;
        System.out.println("multiplication : "+sum);
        //modulus
        sum = numberOne % numberTwo;
        System.out.println("Modulus : "+sum);
        //Increment ++
        sum = numberOne;
        ++sum;
        System.out.println("Increment: " + sum);
        //decrement
        sum = numberOne;
        --sum;
        System.out.println("Decrement: " + sum);








    }
}
